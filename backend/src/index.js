import express from "express";
import dotenv from "dotenv";
import cors from "cors";
import cookieParser from "cookie-parser";
import http from "http";
import { Server } from "socket.io";
import ConnectDB from "./config/db.js";
import authrouter from "./routes/auth.route.js";
import Messagerouter from "./routes/Message.route.js";

dotenv.config();
ConnectDB();

//create express server and HTTP server
const app = express();
const server = http.createServer(app);

//initialize socket.io server
export const io = new Server(server, {
   cors: {
      origin: [process.env.FRONTEND_URL, "https://chatapp-1-3zjl.onrender.com"],
      methods: ["GET", "POST"],
      credentials: true,
   },
});

//Store online User
export const userScoketMap = {};

io.on("connection", (socket) => {
   // server se client ko (single)
   const userId = socket.handshake.query.userId;
   console.log("User Connected", userId);

   if (userId) userScoketMap[userId] = socket.id;

   //Emit online users to all connected clients
   io.emit("getOnlineUsers", Object.keys(userScoketMap));

   socket.on("disconnect", () => {
      console.log("User Disconnected", userId);
      delete userScoketMap[userId];
      io.emit("getOnlineUsers", Object.keys(userScoketMap));
   });
});

// Middlewares
app.use(express.json());
app.use(cookieParser());

app.use(
   cors({
      origin: [process.env.FRONTEND_URL, "https://chatapp-1-3zjl.onrender.com"],
      credentials: true,
   })
);

app.use("/api/v1/auth", authrouter);
app.use("/api/v1/message", Messagerouter);

app.get("/", (req, res) => {
   res.send("hello server");
});

const PORT = process.env.PORT || 7000;

server.listen(PORT, () => {
   console.log(`Server running on http://localhost:${PORT}`);
});
